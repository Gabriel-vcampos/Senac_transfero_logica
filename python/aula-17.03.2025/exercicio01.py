#1) Faça um programa que leia dois números e mostre a soma, a subtração, a multiplicação e a divisão entre eles.

numero_1 = int(input("Digite a seguir um número: "))
numero_2 = int(input("Digite a seguir outro número: "))

soma = numero_1 + numero_2
subtracao = numero_1 - numero_2
multiplicacao = numero_1 * numero_2
divisao = numero_1 / numero_2

print("A soma é: " , soma)
print("A soma é: " , subtracao)
print("A soma é: " , multiplicacao)
print("A soma é: " , divisao)



