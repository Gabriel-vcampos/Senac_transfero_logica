#4) Crie um programa que leia três notas de um aluno e calcule a média aritmética.

nota_1 = int(input("Digite a primeira nota a seguir:"))
nota_2 = int(input("Digite a segunda nota a seguir:"))
nota_3 = int(input("Digite a terceira nota a seguir:"))
media = (nota_1 + nota_2 + nota_3)/3
print("A média dessas notas é:" , media)